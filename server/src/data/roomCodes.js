const MAX_LEN = 6;

export function generateCodingRoomCode() {
  const base =
    CODING_CODE_BASES[Math.floor(Math.random() * CODING_CODE_BASES.length)];
  const trimmed = base.slice(0, MAX_LEN);
  const digitsLeft = MAX_LEN - trimmed.length;
  let suffix = "";
  for (let i = 0; i < digitsLeft; i++) {
    suffix += String(Math.floor(Math.random() * 10));
  }
  return (trimmed + suffix).toUpperCase();
}

export function isValidRoomCodeFormat(code) {
  return /^[A-Z][A-Z0-9]{3,5}$/.test(code?.toUpperCase() ?? "");
}
